void Map::addTile(MapTile* tile)
{
    m_tilesByName.insert(std::pair<std::string, MapTile*>(tile->getName(), tile));
}

MapTile* Map::getTileByName(std::string name)
{
    return m_tilesByName.find(name)->second;
}

