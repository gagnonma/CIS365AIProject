#include "Map.h"

MapTile::MapTile(std::string name)
{
    m_name = name;
}

bool MapTile::isAdjacentToTile(MapTile* tile)
{
    for (auto e : tile->getAdjacentTiles()) {
        if (e == this) {
            return true;
        }
    }
    return false;
}

void MapTile::addAdjacentTile(MapTile* tile)
{
    m_adjacentTiles.insert(tile); //Add the tile to this tile's list of adjacent tiles
    if (!tile->isAdjacentToTile(this))
    {
        tile->addAdjacentTile(this); //Reciprocally connect
    }
}

std::set<MapTile*> MapTile::getAdjacentTiles()
{
    return m_adjacentTiles;
}

std::string MapTile::getName()
{
    return m_name;
}

